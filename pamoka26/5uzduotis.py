number1 = input("Enter first number: ")
number2 = input("Enter second number: ")

sum_result = float(number1) + float(number2)
subtraction_result = float(number1) - float(number2)
multiplication_result = float(number1) * float(number2)
division_result = float(number1) / float(number2)

print(f"Sum: {sum_result}")
print(f"Subtraction: {subtraction_result}")
print(f"Multiplication: {multiplication_result}")
print(f"Division: {division_result}")
